# Exercício 01 - Maior num

num1 = int(input('Informe o 1° número: '))
num2 = int(input('Informe o 2º número: '))

if(num1>num2):
    print(f'O primeiro número é maior que o segundo!')
elif(num2>num1):
    print(f'O segundo número é maior que o primeiro!')
else:
    print('O valor do primeiro número é igual que o segundo!')
